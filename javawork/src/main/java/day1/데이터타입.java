package day1;
/*
 * 값의 종류에 따라 올바른 변수 선언
 * 문자 정수 실수*/
public class 데이터타입 {
	
	public static void main(String[] args) {
		/* 
		 * 
		 * */
		// 반려견의 이름 name변수
		String name = "몽몽";
		// 반려견 나이 age변수
		long age = 4;
		// 반려견 몸무게 weight변수
		double weight = 12.4;
		
		System.out.println("나의 반려견 이름은 : " + name);
		System.out.println("나의 반려견 나이는 : " + age);
		System.out.println("나의 반려견 몸무게는 : " + weight);
	}
}
